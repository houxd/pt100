    use super::*;

    #[test]
    fn test_zero_celsius() {
        // 0°C should correspond to 100000 mΩ (100Ω)
        let temp = 0;
        let resistance = temperature_to_resistance(temp);
        assert_eq!(resistance, 100000);

        let temp_back = resistance_to_temperature(resistance);
        assert_eq!(temp_back, temp);
    }

    #[test]
    fn test_negative_temperature() {
        // Test negative temperature
        let temp = -50_000; // -50°C
        let resistance = temperature_to_resistance(temp);
        let temp_back = resistance_to_temperature(resistance);
        assert!((temp_back - temp).abs() < 100); // Allow small error
    }

    #[test]
    fn test_positive_temperature() {
        // Test positive temperature
        let temp = 100_000; // 100°C
        let resistance = temperature_to_resistance(temp);
        let temp_back = resistance_to_temperature(resistance);
        assert!((temp_back - temp).abs() < 100); // Allow small error
    }

    #[test]
    fn test_boundary_values() {
        // Test boundary values
        let r_min = resistance_to_temperature(R_MIN);
        assert_eq!(r_min, T_MIN);

        let r_max = resistance_to_temperature(R_MAX);
        assert_eq!(r_max, T_MAX);
    }

    #[test]
    fn test_out_of_range() {
        // Test that out-of-range values should be clamped
        let temp = resistance_to_temperature(R_MIN - 1000);
        assert_eq!(temp, T_MIN);

        let temp = resistance_to_temperature(R_MAX + 1000);
        assert_eq!(temp, T_MAX);
    }
