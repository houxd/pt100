/// Convert PT100 resistance value to temperature
///
/// # Arguments
/// * `resistance` - Resistance value (mΩ)
///
/// # Returns
/// Temperature (m°C)
pub fn resistance_to_temperature(resistance: u32) -> i32 {
    // Clamp resistance value within valid range
    let resistance = resistance.clamp(R_MIN, R_MAX);

    // Find the first value in the table greater than the measured resistance
    let mut index = 0;
    while index < PT100_TABLE.len() && resistance > PT100_TABLE[index] {
        index += 1;
    }

    // Boundary check
    if index == 0 {
        index = 1;
    }
    if index > INDEX_MAX {
        index = INDEX_MAX;
    }

    // Select the closest lower resistance value
    index -= 1;

    /*
     * Temperature calculation formula:
     * T = T1 + (T2 - T1) * (R - R1) / (R2 - R1)
     *
     * Where:
     * T  : Calculated temperature
     * T1 : Temperature value in table below measured resistance
     * T2 : Temperature value in table above measured resistance
     * R  : Measured resistance
     * R1 : Resistance value in table below measured resistance
     * R2 : Resistance value in table above measured resistance
     *
     * (Source: Elektrische Temperaturmessung (M. Nau / jumo))
     */
    let t1 = (index as i32 - INDEX_ZERO as i32) * T_DIFF;
    let r1 = PT100_TABLE[index];
    let r2 = PT100_TABLE[index + 1];

    let temperature = t1 + (T_DIFF * (resistance as i32 - r1 as i32)) / (r2 as i32 - r1 as i32);

    temperature
}

/// Convert temperature to PT100 resistance value
///
/// # Arguments
/// * `temperature` - Temperature (m°C)
///
/// # Returns
/// Resistance value (mΩ)
pub fn temperature_to_resistance(temperature: i32) -> u32 {
    // Clamp temperature within valid range
    let temperature = temperature.clamp(T_MIN, T_MAX);

    // Find resistance value
    let temp = temperature + T_MIN.abs();
    let mut index = (temp / T_DIFF) as usize;

    // Limit array index
    if index == INDEX_MAX {
        index = INDEX_MAX - 1;
    }

    /*
     * Resistance calculation formula:
     * R = R1 + (R2 - R1) * (T - T1) / (T2 - T1)
     *
     * Where:
     * T  : Calculated temperature
     * T1 : Temperature value in table below input temperature
     * T2 : Temperature value in table above input temperature
     * R  : Calculated resistance
     * R1 : Resistance value in table below input temperature
     * R2 : Resistance value in table above input temperature
     *
     * (Source: Elektrische Temperaturmessung (M. Nau / jumo))
     */
    let t1 = (temp / T_DIFF) * T_DIFF;
    let r1 = PT100_TABLE[index];
    let r2 = PT100_TABLE[index + 1];

    let resistance = r1 + (((temp - t1) * (r2 as i32 - r1 as i32)) / T_DIFF) as u32;

    resistance
}

